    private void cambioIntestatarioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cambioIntestatarioButtonActionPerformed
        CambioIntestatario cambioIntestatario = new CambioIntestatario(this, true, banca.getNome(), banca.getCognome());
        cambioIntestatario.setVisible(true);
        if(cambioIntestatario.nome == banca.getNome() || cambioIntestatario.cognome == banca.getCognome()) return;
        else if(banca.cambioIntestatario(cambioIntestatario.nome, cambioIntestatario.cognome)) {
            nomeLabel.setText("Nome: " + banca.getNome());
            cognomeLabel.setText("Cognome: " + banca.getCognome());
            this.pack();
            JOptionPane.showMessageDialog(this, "Cambio intestatario effettuato con successo", "Cambio intestatario", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Cambio intestatario non effettuato, riprova più tardi", "Cambio intestatario", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cambioIntestatarioButtonActionPerformed


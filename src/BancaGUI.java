    private void bonificoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bonificoButtonActionPerformed
        Bonifico bonifico = new Bonifico(this, true, banca.getSaldo(), banca.getIban());
        bonifico.setVisible(true);
        if (bonifico.quantita != 0) {
            int result = banca.bonifico(bonifico.ibanRicevente, bonifico.quantita);
            if(result == 0) {
                JOptionPane.showMessageDialog(this, "Bonifico effettuato con successo", "Bonifico", JOptionPane.INFORMATION_MESSAGE);
                saldoLabel.setText("Saldo: " + banca.getSaldo() + "€");
            } else if(result == 1) {
                JOptionPane.showMessageDialog(this, "Non hai abbastanza soldi!", "Bonifico", JOptionPane.ERROR_MESSAGE);
            } else if(result == 2) {
                JOptionPane.showMessageDialog(this, "L'iban inserito non è valido!", "Bonifico", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Errore durante il bonifico", "Bonifico", JOptionPane.ERROR_MESSAGE);
            }
            saldoLabel.setText("Saldo: " + banca.getSaldo() + "€");
        }
    }//GEN-LAST:event_bonificoButtonActionPerformed


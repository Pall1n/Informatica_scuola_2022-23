    private void prelevaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prelevaButtonActionPerformed
        Preleva preleva = new Preleva(this, true, banca.getSaldo());
        preleva.setVisible(true);
        if (preleva.prelievo != 0) {
            if(banca.prelievo(preleva.prelievo)) {
                JOptionPane.showMessageDialog(this, "Versamento effettuato con successo", "Versamento", JOptionPane.INFORMATION_MESSAGE);
                saldoLabel.setText("Saldo: " + banca.getSaldo() + "€");
            } else {
                JOptionPane.showMessageDialog(this, "Errore durante il versamento", "Versamento", JOptionPane.ERROR_MESSAGE);
            }
            saldoLabel.setText("Saldo: " + banca.getSaldo() + "€");
        }
    }//GEN-LAST:event_prelevaButtonActionPerformed


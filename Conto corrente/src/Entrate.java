        if(entrate != null) {
            Object[][] data = new Object[entrate.size()][4];
            int i = 0;
            for (Map.Entry<String, Double> entry : entrate.entrySet()) {
                String[] entryKeySlitted = entry.getKey().split("/");
                if(entryKeySlitted[0].equals("versamento")) {
                    data[i][0] = "Versamento";
                    data[i][2] = "";
                    data[i][2] = entry.getValue() + "€";
                    data[i][3] = entryKeySlitted[1] + " " + entryKeySlitted[2];
                } else {
                    data[i][0] = "Bonifico";
                    data[i][1] = entryKeySlitted[0];
                    data[i][2] = entry.getValue() + "€";
                    data[i][3] = entryKeySlitted[1] + " " + entryKeySlitted[2];
                }
                i++;
            }
            usciteTable.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
        } else {
            Object[][] data = new Object[1][4];
            data[0][0] = "Nessuna entrata";
            usciteTable.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
        }


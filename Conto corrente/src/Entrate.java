    public Entrate(java.awt.Frame parent, boolean modal, String entrate) {
        super(parent, modal);
        if(entrate != "") {
            String[] coppie = entrate.substring(1,entrate.length() - 1).split(", ");
            for (int i=0; i<coppie.length; i++) {
                String coppia = coppie[i];
                String[] chiaveValore = coppia.split("=");
                this.entrate.put(chiaveValore[0], Double.parseDouble(chiaveValore[1]));
            }
        } else {
            this.entrate = null;
        }
        initComponents();
    }

